      SELECT LIKE_RECEIVER, COUNT(LIKE_SENDER) AS S /*좋아요 아이디와 그 수를 COUNT한 값을 출력*/
      FROM TBL_LIKE /*좋아요 버튼에서*/
      GROUP BY LIKE_RECEIVER /*좋아요받은 ID 순으로 정렬*/
   )WHERE S = 
   (
      SELECT MAX(COUNT(LIKE_SENDER)) /*가장많이 받은 좋아요를 조회*/
      FROM TBL_LIKE
      GROUP BY LIKE_RECEIVER /*좋아요 받은 사람기준으로 그룹화*/
   )
);


