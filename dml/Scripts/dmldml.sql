/*AS: 아다리아스(별칭)
 * 
 * 테이블 명 혹은 컬럼명 뒤에 AS를 붙여서 원하는 이름을 설정할 수 있다.
 * 이후부터는 설정한 이름으로 사용하면 된다.
 * SELECT절에는 AS를 작성해도 괜찮지만, FROM절에서는 AS를 작성하면 안된다.
 * 이럴 때에는 AS자리에 띄어쓰기 후 작성하도록 한다.
 * 
 * */
SELECT 
   O.ID "주인 번호", O.OWNER_NAME "SELECT", O.OWNER_AGE, 
   O.OWNER_PHONE, O.OWNER_ADDRESS,
   P.PET_NAME, P.PET_AGE
FROM TBL_OWNER O JOIN TBL_PET P 
ON O.ID = P.OWNER_ID AND P.PET_AGE > 5;

/*CONCATENATION: 연결
 * 
 * "안" + "녕": JAVA
 * '안' || '녕': ORACLE
 * 
 * */

SELECT O.OWNER_NAME || '님의 반려동물은 ' || P.PET_NAME || '입니다.' AS 자기소개
FROM TBL_OWNER O JOIN TBL_PET P
ON O.ID = P.OWNER_ID;

/*
 * LIKE: 포함된 문자열 값을 찾고, 문자의 개수도 제한을 줄 수 있다.
 *
 * %: 모든 것
 * _: 글자 수
 * 
 * 예시
 * '%A'   : A로 끝나는 모든 값
 * 'A%'   : A로 시작하는 모든 값
 * 'A__': A로 시작하면서 3글자인 값
 * '_A'   : A로 끝나면서 2글자인 값
 * '%A%': A가 포함된 값 
 * 
 * */

/*반려동물 이름에서 '뽀'가 포함된 반려동물의 주인 정보 조회*/
/*핸드폰 번호에 '0101234'로 시작하는 주인의 반려동물 전체 조회*/
/*반려동물 병명에 '염'이 들어가있지 않는 반려동물 전체 정보 조회*/


SELECT O.ID, OWNER_NAME, OWNER_AGE, OWNER_PHONE, OWNER_ADDRESS, PET_NAME
FROM TBL_PET P JOIN TBL_OWNER O
ON OWNER_ID = O.ID AND PET_NAME LIKE '%뽀%';

SELECT PET_NAME, PET_AGE, PET_ILL_NAME, OWNER_NAME, OWNER_PHONE
FROM TBL_OWNER O JOIN TBL_PET
ON OWNER_ID = O.ID AND OWNER_PHONE LIKE '0101234%';

SELECT ID, PET_ILL_NAME, PET_NAME, PET_AGE, WEIGHT, OWNER_ID
FROM TBL_PET P
WHERE PET_ILL_NAME NOT LIKE '%염%';


