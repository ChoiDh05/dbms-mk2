   (
      SELECT LIKE_RECEIVER, COUNT(LIKE_SENDER) AS S /*좋아요 받은 사람과 좋아요누른 사람 COUNT조회 */
      FROM TBL_LIKE /*좋아요 테이블에서*/
      GROUP BY LIKE_RECEIVER /*좋아요 받은사람 그룹으로 정리*/
   )WHERE S = /*조건식: S(좋아요누른 사람 수)가 아래의 값과 같아야한다.*/
   (
      SELECT MAX(COUNT(LIKE_SENDER)) /*제일 좋아요를 많이 받은 사람 수 조회*/
      FROM TBL_LIKE /*좋아요 테이블에서*/
      GROUP BY LIKE_RECEIVER
